def t(key: str, **kwargs: object) -> str:
    lang = st.session_state.get("ui_lang", "zh")
    bundle = MESSAGES.get(lang) or MESSAGES["zh"]
    s = bundle.get(key)
    if s is None:
        s = MESSAGES["zh"].get(key, key)
    if kwargs:
        return s.format(**{k: str(v) for k, v in kwargs.items()})
    return s


def account_type_label(code: str) -> str:
    return t(f"acc_type_{code}")


def tx_type_label(code: str) -> str:
    return t(f"tx_type_{code}")


def asset_type_label(code: str) -> str:
    c = (code or "").strip().lower()
    if c in ("fund", "stock"):
        return t(f"asset_type_{c}")
    return (code or "").strip() or "—"
